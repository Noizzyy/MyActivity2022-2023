public class Tao {
 
    private String pangalan;
    private int edad;
 
    public Tao(String pangalan, int edad) {
        this.pangalan = pangalan;
        this.edad = edad;
    }
 
    public String getPangalan() {
        return pangalan;
    }
    public int getEdad() {
        return edad;
    }
 
}